package Kodlamaio;

public class Course {
	
	int id;
	String courseName;
	String teacherName;
	double coursePrice;
	String detail;
	
	
	public Course(int id, String courseName, String teacherName, double coursePrice, String detail) {
		this.id = id ;
		this.courseName = courseName;
		this.teacherName = teacherName;
		this.coursePrice = coursePrice;
		this.detail = detail;
	}
		

}
